    public void RegistrarPaciente(){
        
        String dia = Integer.toString(txtFecha.getCalendar().get(Calendar.DAY_OF_MONTH));
        String mes = Integer.toString(txtFecha.getCalendar().get(Calendar.MONTH)+1);
        String año = Integer.toString(txtFecha.getCalendar().get(Calendar.YEAR));
        String Fecha = (año + "/" + mes + "/" + dia);
        
        try {
            PreparedStatement guardar = conect.prepareStatement("INSERT INTO registro_paciente (paciente_dni,paciente_nombre,paciente_nacimiento,paciente_celular,paciente_direccion,paciente_correo,paciente_historial,paciente_genero) VALUES (?,?,?,?,?,?,?,?)");
            guardar.setString(1, txtDni.getText());
            guardar.setString(2, txtNombre.getText());
            guardar.setString(3, Fecha);
            guardar.setString(4, txtTelefono.getText());
            guardar.setString(5, txtDireccion.getText());
            guardar.setString(6, txtCorreo.getText());
            guardar.setString(7, txtHistorial.getText());
            guardar.setString(8, txtGenero.getSelectedItem().toString());
            guardar.executeUpdate();
            
            PreparedStatement guardar1 = conect.prepareStatement("INSERT INTO paciente (paciente_dni,paciente_nombre,paciente_nacimiento,paciente_celular,paciente_direccion,paciente_correo,paciente_historial) VALUES (?,?,?,?,?,?,?)");
            guardar1.setString(1, txtDni.getText());
            guardar1.setString(2, txtNombre.getText());
            guardar1.setString(3, Fecha);
            guardar1.setString(4, txtTelefono.getText());
            guardar1.setString(5, txtDireccion.getText());
            guardar1.setString(6, txtCorreo.getText());
            guardar1.setString(7, txtHistorial.getText());
            guardar1.executeUpdate();
            JOptionPane.showMessageDialog(null, "Paciente registrado");
            
            txtDni.setText("");
            txtNombre.setText("");
            Fecha = " ";
            txtTelefono.setText("");
            txtDireccion.setText("");
            txtCorreo.setText("");
            txtHistorial.setText("");
            
        } catch (HeadlessException | SQLException e) {
            JOptionPane.showMessageDialog(null, e+" No se logró registrar al paciente");
        }
    }
    
    
